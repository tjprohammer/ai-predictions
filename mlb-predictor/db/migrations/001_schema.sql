-- MLB Predictor canonical shared-backbone schema.
-- Safe to re-run because objects are created with IF NOT EXISTS.

CREATE TABLE IF NOT EXISTS dim_teams (
    team_id          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    team_abbr        VARCHAR(3) NOT NULL UNIQUE,
    team_name        TEXT NOT NULL,
    league           VARCHAR(2),
    division         TEXT,
    created_at       TIMESTAMPTZ DEFAULT now(),
    updated_at       TIMESTAMPTZ DEFAULT now()
);
ALTER TABLE dim_teams ALTER COLUMN division TYPE TEXT;

CREATE TABLE IF NOT EXISTS dim_players (
    player_id        BIGINT PRIMARY KEY,
    full_name        TEXT NOT NULL,
    first_name       TEXT,
    last_name        TEXT,
    bats             CHAR(1),
    throws           CHAR(1),
    position         VARCHAR(5),
    team_abbr        VARCHAR(3),
    active           BOOLEAN DEFAULT TRUE,
    created_at       TIMESTAMPTZ DEFAULT now(),
    updated_at       TIMESTAMPTZ DEFAULT now()
);
CREATE INDEX IF NOT EXISTS idx_dim_players_team ON dim_players(team_abbr);

CREATE TABLE IF NOT EXISTS dim_venues (
    venue_id         BIGINT PRIMARY KEY,
    venue_name       TEXT NOT NULL,
    city             TEXT,
    state            VARCHAR(32),
    latitude         NUMERIC(9,6),
    longitude        NUMERIC(9,6),
    elevation_ft     INTEGER,
    roof_type        VARCHAR(20),
    timezone_name    TEXT,
    created_at       TIMESTAMPTZ DEFAULT now(),
    updated_at       TIMESTAMPTZ DEFAULT now()
);

CREATE TABLE IF NOT EXISTS dim_dates (
    date_id          DATE PRIMARY KEY,
    year             SMALLINT NOT NULL,
    month            SMALLINT NOT NULL,
    day              SMALLINT NOT NULL,
    day_of_week      SMALLINT NOT NULL,
    week_of_year     SMALLINT,
    is_weekend       BOOLEAN,
    season_phase     VARCHAR(20),
    created_at       TIMESTAMPTZ DEFAULT now(),
    updated_at       TIMESTAMPTZ DEFAULT now()
);

CREATE TABLE IF NOT EXISTS games (
    game_id          BIGINT PRIMARY KEY,
    game_date        DATE NOT NULL,
    season           SMALLINT,
    game_type        VARCHAR(5) DEFAULT 'R',
    status           VARCHAR(20) DEFAULT 'scheduled',
    home_team        VARCHAR(3) NOT NULL,
    away_team        VARCHAR(3) NOT NULL,
    venue_id         BIGINT,
    venue_name       TEXT,
    game_start_ts    TIMESTAMPTZ,
    resumed_from     BIGINT,
    double_header    SMALLINT DEFAULT 0,
    innings          SMALLINT DEFAULT 9,
    home_runs        SMALLINT,
    away_runs        SMALLINT,
    total_runs       SMALLINT,
    home_hits        SMALLINT,
    away_hits        SMALLINT,
    home_errors      SMALLINT,
    away_errors      SMALLINT,
    home_win         BOOLEAN,
    created_at       TIMESTAMPTZ DEFAULT now(),
    updated_at       TIMESTAMPTZ DEFAULT now()
);
CREATE INDEX IF NOT EXISTS idx_games_date ON games(game_date);
CREATE INDEX IF NOT EXISTS idx_games_teams_date ON games(game_date, home_team, away_team);

CREATE TABLE IF NOT EXISTS game_weather (
    weather_id       BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    game_id          BIGINT NOT NULL,
    game_date        DATE NOT NULL,
    snapshot_ts      TIMESTAMPTZ NOT NULL,
    source_name      TEXT NOT NULL,
    weather_type     VARCHAR(20) DEFAULT 'forecast',
    temperature_f    NUMERIC(5,2),
    wind_speed_mph   NUMERIC(5,2),
    wind_direction_deg NUMERIC(6,2),
    humidity_pct     NUMERIC(5,2),
    precipitation_pct NUMERIC(5,2),
    pressure_hpa     NUMERIC(7,2),
    roof_open_flag   BOOLEAN,
    raw_payload      JSONB,
    created_at       TIMESTAMPTZ DEFAULT now(),
    updated_at       TIMESTAMPTZ DEFAULT now(),
    UNIQUE (game_id, snapshot_ts, source_name)
);
CREATE INDEX IF NOT EXISTS idx_game_weather_game ON game_weather(game_id, snapshot_ts DESC);

CREATE TABLE IF NOT EXISTS game_markets (
    market_id        BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    game_id          BIGINT NOT NULL,
    game_date        DATE NOT NULL,
    sportsbook       TEXT NOT NULL,
    market_type      VARCHAR(32) NOT NULL,
    line_value       NUMERIC(5,2),
    over_price       INTEGER,
    under_price      INTEGER,
    snapshot_ts      TIMESTAMPTZ NOT NULL,
    is_opening       BOOLEAN DEFAULT FALSE,
    is_closing       BOOLEAN DEFAULT FALSE,
    source_name      TEXT,
    created_at       TIMESTAMPTZ DEFAULT now(),
    updated_at       TIMESTAMPTZ DEFAULT now(),
    UNIQUE (game_id, sportsbook, market_type, snapshot_ts)
);
CREATE INDEX IF NOT EXISTS idx_game_markets_game ON game_markets(game_id, snapshot_ts DESC);

CREATE TABLE IF NOT EXISTS player_prop_markets (
    prop_market_id    BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    game_id           BIGINT NOT NULL,
    game_date         DATE NOT NULL,
    player_id         BIGINT NOT NULL,
    player_name       TEXT,
    team              VARCHAR(3),
    sportsbook        TEXT NOT NULL,
    market_type       VARCHAR(32) NOT NULL,
    line_value        NUMERIC(5,2),
    over_price        INTEGER,
    under_price       INTEGER,
    snapshot_ts       TIMESTAMPTZ NOT NULL,
    is_opening        BOOLEAN DEFAULT FALSE,
    is_closing        BOOLEAN DEFAULT FALSE,
    source_name       TEXT,
    created_at        TIMESTAMPTZ DEFAULT now(),
    updated_at        TIMESTAMPTZ DEFAULT now(),
    UNIQUE (game_id, player_id, sportsbook, market_type, snapshot_ts)
);
CREATE INDEX IF NOT EXISTS idx_player_prop_markets_game ON player_prop_markets(game_id, snapshot_ts DESC);
CREATE INDEX IF NOT EXISTS idx_player_prop_markets_player ON player_prop_markets(player_id, game_date);

CREATE TABLE IF NOT EXISTS lineups (
    lineup_id        BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    game_id          BIGINT NOT NULL,
    game_date        DATE NOT NULL,
    team             VARCHAR(3) NOT NULL,
    player_id        BIGINT NOT NULL,
    player_name      TEXT NOT NULL,
    lineup_slot      SMALLINT,
    field_position   VARCHAR(5),
    batting_order    SMALLINT,
    is_confirmed     BOOLEAN DEFAULT FALSE,
    source_name      TEXT NOT NULL,
    source_url       TEXT,
    snapshot_ts      TIMESTAMPTZ NOT NULL,
    created_at       TIMESTAMPTZ DEFAULT now(),
    updated_at       TIMESTAMPTZ DEFAULT now(),
    UNIQUE (game_id, player_id, source_name, snapshot_ts)
);
CREATE INDEX IF NOT EXISTS idx_lineups_game ON lineups(game_id, snapshot_ts DESC);
CREATE INDEX IF NOT EXISTS idx_lineups_player ON lineups(player_id, game_date);

