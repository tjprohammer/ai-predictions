CREATE TABLE IF NOT EXISTS player_status_daily (
    status_snapshot_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    game_date          DATE NOT NULL,
    team_id            BIGINT,
    team               VARCHAR(3) NOT NULL,
    player_id          BIGINT NOT NULL,
    player_name        TEXT NOT NULL,
    position           VARCHAR(5),
    jersey_number      VARCHAR(8),
    roster_type        VARCHAR(32) NOT NULL,
    status_code        VARCHAR(16),
    status_description TEXT,
    status_note        TEXT,
    availability_bucket VARCHAR(20),
    is_active_roster   BOOLEAN DEFAULT FALSE,
    is_available       BOOLEAN DEFAULT FALSE,
    is_injured         BOOLEAN DEFAULT FALSE,
    source_name        TEXT NOT NULL,
    source_url         TEXT,
    snapshot_ts        TIMESTAMPTZ NOT NULL,
    raw_payload        JSONB,
    created_at         TIMESTAMPTZ DEFAULT now(),
    updated_at         TIMESTAMPTZ DEFAULT now(),
    UNIQUE (game_date, team, player_id, roster_type, source_name, snapshot_ts)
);

CREATE INDEX IF NOT EXISTS idx_player_status_daily_team ON player_status_daily(game_date, team, snapshot_ts DESC);
CREATE INDEX IF NOT EXISTS idx_player_status_daily_player ON player_status_daily(player_id, game_date);