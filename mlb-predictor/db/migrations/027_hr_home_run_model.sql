-- Home run probability (player game): features + model predictions.
CREATE TABLE IF NOT EXISTS player_features_hr (
    feature_row_id    BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    game_id           BIGINT NOT NULL,
    game_date         DATE NOT NULL,
    player_id         BIGINT NOT NULL,
    team              VARCHAR(3) NOT NULL,
    opponent          VARCHAR(3),
    prediction_ts     TIMESTAMPTZ NOT NULL,
    game_start_ts     TIMESTAMPTZ,
    line_snapshot_ts  TIMESTAMPTZ,
    feature_cutoff_ts TIMESTAMPTZ NOT NULL,
    feature_version   TEXT NOT NULL,
    feature_payload   JSONB NOT NULL,
    got_hr            BOOLEAN,
    created_at        TIMESTAMPTZ DEFAULT now(),
    updated_at        TIMESTAMPTZ DEFAULT now(),
    UNIQUE (game_id, player_id, feature_cutoff_ts, feature_version)
);
CREATE INDEX IF NOT EXISTS idx_player_features_hr_game ON player_features_hr(game_date, game_id);
CREATE INDEX IF NOT EXISTS idx_player_features_hr_player ON player_features_hr(player_id, game_date);

CREATE TABLE IF NOT EXISTS predictions_player_hr (
    prediction_id               BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    game_id                     BIGINT NOT NULL,
    game_date                   DATE NOT NULL,
    player_id                   BIGINT NOT NULL,
    team                        VARCHAR(3) NOT NULL,
    prediction_ts               TIMESTAMPTZ NOT NULL,
    model_name                  TEXT NOT NULL,
    model_version               TEXT NOT NULL,
    predicted_hr_probability    NUMERIC(7,5) NOT NULL,
    fair_price                  INTEGER,
    market_price                INTEGER,
    edge                        NUMERIC(7,5),
    reasoning_json              TEXT,
    created_at                  TIMESTAMPTZ DEFAULT now(),
    updated_at                  TIMESTAMPTZ DEFAULT now(),
    UNIQUE (game_id, player_id, prediction_ts, model_name, model_version)
);
CREATE INDEX IF NOT EXISTS idx_predictions_player_hr_date ON predictions_player_hr(game_date, game_id);
