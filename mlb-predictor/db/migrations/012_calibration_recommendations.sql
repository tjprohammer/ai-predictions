CREATE TABLE IF NOT EXISTS prediction_calibration_bins (
    bin_id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    score_date          TEXT NOT NULL,
    market              TEXT NOT NULL,
    model_name          TEXT NOT NULL,
    model_version       TEXT,
    bin_lower           NUMERIC(4,2) NOT NULL,
    bin_upper           NUMERIC(4,2) NOT NULL,
    bin_label           TEXT NOT NULL,
    count               INTEGER NOT NULL DEFAULT 0,
    actual_hit_rate     NUMERIC(6,4),
    mean_predicted_prob NUMERIC(6,4),
    brier_score_sum     NUMERIC(12,6),
    UNIQUE (score_date, market, model_name, bin_label)
);
CREATE INDEX IF NOT EXISTS idx_calibration_bins_date ON prediction_calibration_bins(score_date, market);

CREATE TABLE IF NOT EXISTS recommendation_history (
    rec_id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    game_date           TEXT NOT NULL,
    game_id             BIGINT NOT NULL,
    market              TEXT NOT NULL,
    entity_type         TEXT NOT NULL DEFAULT 'game',
    entity_id           BIGINT,
    player_id           BIGINT,
    team                TEXT,
    away_team           TEXT,
    home_team           TEXT,
    model_name          TEXT NOT NULL,
    model_version       TEXT,
    recommended_side    TEXT,
    probability         NUMERIC(6,4),
    market_line         NUMERIC(6,2),
    predicted_value     NUMERIC(8,3),
    actual_value        NUMERIC(8,3),
    actual_side         TEXT,
    graded              BOOLEAN DEFAULT FALSE,
    success             BOOLEAN,
    edge                NUMERIC(6,4),
    created_at          TIMESTAMPTZ DEFAULT now(),
    UNIQUE (game_id, market, entity_id, model_name)
);
CREATE INDEX IF NOT EXISTS idx_recommendation_history_date ON recommendation_history(game_date, market);
CREATE INDEX IF NOT EXISTS idx_recommendation_history_model ON recommendation_history(model_name, game_date);
