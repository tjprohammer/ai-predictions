CREATE TABLE IF NOT EXISTS market_snapshot_versions (
    snapshot_version_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    game_id             BIGINT NOT NULL,
    game_date           TEXT NOT NULL,
    sportsbook          TEXT NOT NULL,
    market_type         TEXT NOT NULL,
    line_value          NUMERIC(5,2),
    over_price          INTEGER,
    under_price         INTEGER,
    snapshot_ts         TIMESTAMPTZ NOT NULL,
    source_name         TEXT,
    pull_number         INTEGER NOT NULL DEFAULT 1,
    created_at          TIMESTAMPTZ DEFAULT now(),
    UNIQUE (game_id, sportsbook, market_type, snapshot_ts, pull_number)
);
CREATE INDEX IF NOT EXISTS idx_market_snapshot_versions_game ON market_snapshot_versions(game_id, market_type);
CREATE INDEX IF NOT EXISTS idx_market_snapshot_versions_date ON market_snapshot_versions(game_date, game_id);

CREATE TABLE IF NOT EXISTS market_selection_freezes (
    freeze_id           BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    game_id             BIGINT NOT NULL,
    market_type         TEXT NOT NULL,
    frozen_sportsbook   TEXT NOT NULL,
    frozen_line_value   NUMERIC(5,2),
    frozen_snapshot_ts  TIMESTAMPTZ NOT NULL,
    frozen_at           TIMESTAMPTZ DEFAULT now(),
    reason              TEXT NOT NULL DEFAULT 'pregame_best',
    UNIQUE (game_id, market_type)
);
CREATE INDEX IF NOT EXISTS idx_market_selection_freezes_game ON market_selection_freezes(game_id);
