-- Trained inning-1 NRFI probability (no run in 1st = combined inning-1 runs == 0).
CREATE TABLE IF NOT EXISTS predictions_inning1_nrfi (
    prediction_id               BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    game_id                     BIGINT NOT NULL,
    game_date                   DATE NOT NULL,
    prediction_ts               TIMESTAMPTZ NOT NULL,
    model_name                  TEXT NOT NULL,
    model_version               TEXT NOT NULL,
    predicted_nrfi_probability  NUMERIC(7, 5) NOT NULL,
    predicted_yrfi_probability  NUMERIC(7, 5),
    recommended_side            TEXT NOT NULL,
    confidence_level            TEXT,
    suppress_reason             TEXT,
    created_at                  TIMESTAMPTZ DEFAULT now(),
    updated_at                  TIMESTAMPTZ DEFAULT now(),
    UNIQUE (game_id, prediction_ts, model_name, model_version)
);
CREATE INDEX IF NOT EXISTS idx_predictions_inning1_nrfi_game ON predictions_inning1_nrfi(game_date, game_id);
