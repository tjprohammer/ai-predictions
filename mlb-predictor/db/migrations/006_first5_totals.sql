ALTER TABLE games
ADD COLUMN IF NOT EXISTS home_runs_first5 SMALLINT,
ADD COLUMN IF NOT EXISTS away_runs_first5 SMALLINT,
ADD COLUMN IF NOT EXISTS total_runs_first5 SMALLINT;

CREATE TABLE IF NOT EXISTS game_features_first5_totals (
    feature_row_id            BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    game_id                   BIGINT NOT NULL,
    game_date                 DATE NOT NULL,
    home_team                 VARCHAR(3) NOT NULL,
    away_team                 VARCHAR(3) NOT NULL,
    prediction_ts             TIMESTAMPTZ NOT NULL,
    game_start_ts             TIMESTAMPTZ,
    line_snapshot_ts          TIMESTAMPTZ,
    feature_cutoff_ts         TIMESTAMPTZ NOT NULL,
    feature_version           TEXT NOT NULL,
    feature_payload           JSONB NOT NULL,
    actual_total_runs_first5  SMALLINT,
    created_at                TIMESTAMPTZ DEFAULT now(),
    updated_at                TIMESTAMPTZ DEFAULT now(),
    UNIQUE (game_id, feature_cutoff_ts, feature_version)
);
CREATE INDEX IF NOT EXISTS idx_game_features_first5_totals_game ON game_features_first5_totals(game_date, game_id);

CREATE TABLE IF NOT EXISTS predictions_first5_totals (
    prediction_id         BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    game_id               BIGINT NOT NULL,
    game_date             DATE NOT NULL,
    prediction_ts         TIMESTAMPTZ NOT NULL,
    model_name            TEXT NOT NULL,
    model_version         TEXT NOT NULL,
    predicted_total_runs  NUMERIC(6,3) NOT NULL,
    over_probability      NUMERIC(6,4),
    under_probability     NUMERIC(6,4),
    market_total          NUMERIC(5,2),
    edge                  NUMERIC(6,4),
    created_at            TIMESTAMPTZ DEFAULT now(),
    updated_at            TIMESTAMPTZ DEFAULT now(),
    UNIQUE (game_id, prediction_ts, model_name, model_version)
);
CREATE INDEX IF NOT EXISTS idx_predictions_first5_totals_game ON predictions_first5_totals(game_date, game_id);