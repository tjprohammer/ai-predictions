-- 023: Batter total bases prediction lane
-- Note: total_bases is computed at runtime in total_bases_builder from
--       singles/doubles/triples/home_runs — no column needed in player_game_batting.

-- Feature snapshot table for the total bases model
CREATE TABLE IF NOT EXISTS game_features_total_bases (
    feature_row_id    BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    game_id           BIGINT NOT NULL,
    game_date         DATE NOT NULL,
    player_id         BIGINT NOT NULL,
    player_name       TEXT,
    team              VARCHAR(3) NOT NULL,
    opponent          VARCHAR(3),
    prediction_ts     TIMESTAMPTZ NOT NULL,
    game_start_ts     TIMESTAMPTZ,
    line_snapshot_ts  TIMESTAMPTZ,
    feature_cutoff_ts TIMESTAMPTZ NOT NULL,
    feature_version   TEXT NOT NULL,
    feature_payload   JSONB NOT NULL,
    actual_total_bases SMALLINT,
    created_at        TIMESTAMPTZ DEFAULT now(),
    updated_at        TIMESTAMPTZ DEFAULT now(),
    UNIQUE (game_id, player_id, feature_cutoff_ts, feature_version)
);

CREATE INDEX IF NOT EXISTS idx_game_features_tb_game ON game_features_total_bases(game_date, game_id);
CREATE INDEX IF NOT EXISTS idx_game_features_tb_player ON game_features_total_bases(player_id, game_date);

-- Predictions table
CREATE TABLE IF NOT EXISTS predictions_total_bases (
    prediction_id       BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    game_id             BIGINT NOT NULL,
    game_date           DATE NOT NULL,
    player_id           BIGINT NOT NULL,
    player_name         TEXT,
    team                VARCHAR(3),
    prediction_ts       TIMESTAMPTZ NOT NULL,
    model_name          TEXT NOT NULL,
    model_version       TEXT NOT NULL,
    predicted_tb        NUMERIC(5,2) NOT NULL,
    over_probability    NUMERIC(6,4),
    under_probability   NUMERIC(6,4),
    market_line         NUMERIC(4,1),
    market_over_price   INTEGER,
    market_under_price  INTEGER,
    edge                NUMERIC(6,4),
    created_at          TIMESTAMPTZ DEFAULT now(),
    updated_at          TIMESTAMPTZ DEFAULT now(),
    UNIQUE (game_id, player_id, prediction_ts, model_name, model_version)
);

CREATE INDEX IF NOT EXISTS idx_pred_tb_game ON predictions_total_bases(game_date, game_id);
