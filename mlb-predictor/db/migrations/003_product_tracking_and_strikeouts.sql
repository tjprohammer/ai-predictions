-- Product tracking surfaces and pitcher strikeout prediction lane.

CREATE TABLE IF NOT EXISTS player_trend_daily (
    game_date             DATE NOT NULL,
    player_id             BIGINT NOT NULL,
    team                  VARCHAR(3) NOT NULL,
    opponent              VARCHAR(3),
    lineup_slot           SMALLINT,
    hits                  SMALLINT,
    at_bats               SMALLINT,
    plate_appearances     SMALLINT,
    had_hit               BOOLEAN,
    hit_streak            SMALLINT,
    rolling_hit_rate_3    NUMERIC(6,4),
    rolling_hit_rate_5    NUMERIC(6,4),
    rolling_hit_rate_7    NUMERIC(6,4),
    rolling_hit_rate_14   NUMERIC(6,4),
    rolling_batting_avg_7 NUMERIC(6,4),
    rolling_batting_avg_14 NUMERIC(6,4),
    rolling_xwoba_7       NUMERIC(6,4),
    rolling_xwoba_14      NUMERIC(6,4),
    rolling_hard_hit_pct_7 NUMERIC(6,4),
    rolling_hard_hit_pct_14 NUMERIC(6,4),
    created_at            TIMESTAMPTZ DEFAULT now(),
    updated_at            TIMESTAMPTZ DEFAULT now(),
    PRIMARY KEY (game_date, player_id)
);
CREATE INDEX IF NOT EXISTS idx_player_trend_daily_player ON player_trend_daily(player_id, game_date DESC);

CREATE TABLE IF NOT EXISTS pitcher_trend_daily (
    game_date               DATE NOT NULL,
    pitcher_id              BIGINT NOT NULL,
    team                    VARCHAR(3) NOT NULL,
    opponent                VARCHAR(3),
    is_probable             BOOLEAN,
    innings_pitched         NUMERIC(4,1),
    strikeouts              SMALLINT,
    walks                   SMALLINT,
    pitches_thrown          SMALLINT,
    whiff_pct               NUMERIC(6,4),
    csw_pct                 NUMERIC(6,4),
    xwoba_against           NUMERIC(6,4),
    days_rest               SMALLINT,
    rolling_strikeouts_3    NUMERIC(6,4),
    rolling_strikeouts_5    NUMERIC(6,4),
    rolling_ip_3            NUMERIC(6,4),
    rolling_ip_5            NUMERIC(6,4),
    rolling_pitch_count_3   NUMERIC(7,4),
    rolling_whiff_pct_3     NUMERIC(6,4),
    rolling_csw_pct_3       NUMERIC(6,4),
    rolling_k_per_batter_5  NUMERIC(6,4),
    created_at              TIMESTAMPTZ DEFAULT now(),
    updated_at              TIMESTAMPTZ DEFAULT now(),
    PRIMARY KEY (game_date, pitcher_id)
);
CREATE INDEX IF NOT EXISTS idx_pitcher_trend_daily_pitcher ON pitcher_trend_daily(pitcher_id, game_date DESC);

CREATE TABLE IF NOT EXISTS game_features_pitcher_strikeouts (
    feature_row_id       BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    game_id              BIGINT NOT NULL,
    game_date            DATE NOT NULL,
    pitcher_id           BIGINT NOT NULL,
    team                 VARCHAR(3) NOT NULL,
    opponent             VARCHAR(3),
    prediction_ts        TIMESTAMPTZ NOT NULL,
    game_start_ts        TIMESTAMPTZ,
    line_snapshot_ts     TIMESTAMPTZ,
    feature_cutoff_ts    TIMESTAMPTZ NOT NULL,
    feature_version      TEXT NOT NULL,
    feature_payload      JSONB NOT NULL,
    actual_strikeouts    SMALLINT,
    created_at           TIMESTAMPTZ DEFAULT now(),
    updated_at           TIMESTAMPTZ DEFAULT now(),
    UNIQUE (game_id, pitcher_id, feature_cutoff_ts, feature_version)
);
CREATE INDEX IF NOT EXISTS idx_game_features_pitcher_strikeouts_game ON game_features_pitcher_strikeouts(game_date, game_id);
CREATE INDEX IF NOT EXISTS idx_game_features_pitcher_strikeouts_pitcher ON game_features_pitcher_strikeouts(pitcher_id, game_date);

CREATE TABLE IF NOT EXISTS predictions_pitcher_strikeouts (
    prediction_id        BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    game_id              BIGINT NOT NULL,
    game_date            DATE NOT NULL,
    pitcher_id           BIGINT NOT NULL,
    team                 VARCHAR(3) NOT NULL,
    prediction_ts        TIMESTAMPTZ NOT NULL,
    model_name           TEXT NOT NULL,
    model_version        TEXT NOT NULL,
    predicted_strikeouts NUMERIC(6,3) NOT NULL,
    over_probability     NUMERIC(6,4),
    under_probability    NUMERIC(6,4),
    market_line          NUMERIC(5,2),
    edge                 NUMERIC(6,4),
    created_at           TIMESTAMPTZ DEFAULT now(),
    updated_at           TIMESTAMPTZ DEFAULT now(),
    UNIQUE (game_id, pitcher_id, prediction_ts, model_name, model_version)
);
CREATE INDEX IF NOT EXISTS idx_predictions_pitcher_strikeouts_game ON predictions_pitcher_strikeouts(game_date, game_id);
CREATE INDEX IF NOT EXISTS idx_predictions_pitcher_strikeouts_pitcher ON predictions_pitcher_strikeouts(pitcher_id, game_date);

CREATE TABLE IF NOT EXISTS prediction_outcomes_daily (
    outcome_id           BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    game_date            DATE NOT NULL,
    market               TEXT NOT NULL,
    entity_type          TEXT NOT NULL,
    entity_id            BIGINT NOT NULL,
    game_id              BIGINT,
    player_id            BIGINT,
    pitcher_id           BIGINT,
    team                 VARCHAR(3),
    opponent             VARCHAR(3),
    model_name           TEXT NOT NULL,
    model_version        TEXT NOT NULL,
    prediction_ts        TIMESTAMPTZ NOT NULL,
    predicted_value      NUMERIC(8,4),
    actual_value         NUMERIC(8,4),
    market_line          NUMERIC(8,4),
    probability          NUMERIC(6,4),
    opposite_probability NUMERIC(6,4),
    recommended_side     TEXT,
    actual_side          TEXT,
    graded               BOOLEAN DEFAULT FALSE,
    success              BOOLEAN,
    beat_market          BOOLEAN,
    absolute_error       NUMERIC(8,4),
    squared_error        NUMERIC(10,4),
    brier_score          NUMERIC(10,6),
    meta_payload         JSONB,
    created_at           TIMESTAMPTZ DEFAULT now(),
    updated_at           TIMESTAMPTZ DEFAULT now(),
    UNIQUE (game_date, market, entity_type, entity_id, model_name, model_version, prediction_ts)
);
CREATE INDEX IF NOT EXISTS idx_prediction_outcomes_daily_date ON prediction_outcomes_daily(game_date, market);
CREATE INDEX IF NOT EXISTS idx_prediction_outcomes_daily_player ON prediction_outcomes_daily(player_id, game_date) WHERE player_id IS NOT NULL;
CREATE INDEX IF NOT EXISTS idx_prediction_outcomes_daily_pitcher ON prediction_outcomes_daily(pitcher_id, game_date) WHERE pitcher_id IS NOT NULL;

CREATE TABLE IF NOT EXISTS model_scorecards_daily (
    score_date           DATE NOT NULL,
    market               TEXT NOT NULL,
    model_name           TEXT NOT NULL,
    model_version        TEXT NOT NULL,
    predictions_count    INTEGER DEFAULT 0,
    graded_count         INTEGER DEFAULT 0,
    success_count        INTEGER DEFAULT 0,
    pending_count        INTEGER DEFAULT 0,
    success_rate         NUMERIC(6,4),
    avg_absolute_error   NUMERIC(8,4),
    avg_bias             NUMERIC(8,4),
    brier_score          NUMERIC(10,6),
    beat_market_rate     NUMERIC(6,4),
    created_at           TIMESTAMPTZ DEFAULT now(),
    updated_at           TIMESTAMPTZ DEFAULT now(),
    PRIMARY KEY (score_date, market, model_name, model_version)
);
CREATE INDEX IF NOT EXISTS idx_model_scorecards_daily_market ON model_scorecards_daily(market, score_date DESC);