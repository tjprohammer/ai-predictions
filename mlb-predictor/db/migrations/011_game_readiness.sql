CREATE TABLE IF NOT EXISTS game_readiness (
    readiness_id        BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    game_id             BIGINT NOT NULL,
    game_date           TEXT NOT NULL,
    away_team           TEXT,
    home_team           TEXT,
    has_away_starter    BOOLEAN DEFAULT FALSE,
    has_home_starter    BOOLEAN DEFAULT FALSE,
    has_market           BOOLEAN DEFAULT FALSE,
    has_venue            BOOLEAN DEFAULT FALSE,
    has_away_lineup      BOOLEAN DEFAULT FALSE,
    has_home_lineup      BOOLEAN DEFAULT FALSE,
    has_weather          BOOLEAN DEFAULT FALSE,
    checks_passed       INTEGER NOT NULL DEFAULT 0,
    checks_total        INTEGER NOT NULL DEFAULT 0,
    warnings            TEXT,
    badge               TEXT NOT NULL DEFAULT 'red',
    validated_at        TIMESTAMPTZ DEFAULT now(),
    UNIQUE (game_id, game_date)
);
CREATE INDEX IF NOT EXISTS idx_game_readiness_date ON game_readiness(game_date, badge);
