CREATE TABLE IF NOT EXISTS pipeline_runs (
    run_id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    job_id              TEXT NOT NULL,
    action              TEXT NOT NULL,
    label               TEXT NOT NULL,
    target_date         TEXT NOT NULL,
    status              TEXT NOT NULL DEFAULT 'queued',
    total_steps         INTEGER NOT NULL DEFAULT 0,
    completed_steps     INTEGER NOT NULL DEFAULT 0,
    error               TEXT,
    created_at          TIMESTAMPTZ DEFAULT now(),
    started_at          TIMESTAMPTZ,
    finished_at         TIMESTAMPTZ,
    UNIQUE (job_id)
);
CREATE INDEX IF NOT EXISTS idx_pipeline_runs_action ON pipeline_runs(action, target_date);
CREATE INDEX IF NOT EXISTS idx_pipeline_runs_created ON pipeline_runs(created_at);

CREATE TABLE IF NOT EXISTS pipeline_run_steps (
    step_id             BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    job_id              TEXT NOT NULL,
    step_index          INTEGER NOT NULL,
    module_name         TEXT NOT NULL,
    returncode          INTEGER,
    stdout              TEXT,
    stderr              TEXT,
    started_at          TIMESTAMPTZ DEFAULT now(),
    finished_at         TIMESTAMPTZ,
    UNIQUE (job_id, step_index)
);
CREATE INDEX IF NOT EXISTS idx_pipeline_run_steps_job ON pipeline_run_steps(job_id);
