-- 022: Home plate umpire assignments per game
-- Populated by src/ingestors/umpire.py using the MLB Stats API schedule/officials hydration.
-- Historical K-rate and run-value adjustments are computed at feature-build time from
-- this table joined to pitcher_starts and games.

CREATE TABLE IF NOT EXISTS umpire_assignments (
    assignment_id   BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    game_id         BIGINT NOT NULL,
    game_date       DATE NOT NULL,
    umpire_name     TEXT NOT NULL,
    umpire_id       INTEGER,
    snapshot_ts     TIMESTAMPTZ NOT NULL,
    created_at      TIMESTAMPTZ DEFAULT now(),
    updated_at      TIMESTAMPTZ DEFAULT now(),
    UNIQUE (game_id, snapshot_ts)
);

CREATE INDEX IF NOT EXISTS idx_umpire_assignments_game ON umpire_assignments(game_id);
CREATE INDEX IF NOT EXISTS idx_umpire_assignments_name ON umpire_assignments(umpire_name, game_date);
