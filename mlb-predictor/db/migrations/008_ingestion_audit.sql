CREATE TABLE IF NOT EXISTS raw_ingest_events (
    event_id            BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    source_name         TEXT NOT NULL,
    ingestor_module     TEXT NOT NULL,
    target_date         TEXT NOT NULL,
    game_id             BIGINT,
    row_count           INTEGER NOT NULL DEFAULT 0,
    parse_status        TEXT NOT NULL DEFAULT 'ok',
    validation_status   TEXT NOT NULL DEFAULT 'ok',
    payload_hash        TEXT,
    warning_flags       TEXT,
    error_message       TEXT,
    ingested_at         TIMESTAMPTZ DEFAULT now(),
    duration_seconds    NUMERIC(8,2)
);
CREATE INDEX IF NOT EXISTS idx_raw_ingest_events_source ON raw_ingest_events(source_name, target_date);
CREATE INDEX IF NOT EXISTS idx_raw_ingest_events_date ON raw_ingest_events(target_date, ingested_at);

CREATE TABLE IF NOT EXISTS source_health (
    health_id           BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    source_name         TEXT NOT NULL,
    checked_at          TIMESTAMPTZ DEFAULT now(),
    is_available        BOOLEAN NOT NULL DEFAULT TRUE,
    response_time_ms    INTEGER,
    error_message       TEXT,
    UNIQUE (source_name, checked_at)
);
CREATE INDEX IF NOT EXISTS idx_source_health_source ON source_health(source_name, checked_at);
