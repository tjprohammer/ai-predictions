log = get_logger(__name__)

ODDS_API_URL = "https://api.the-odds-api.com/v4/sports/baseball_mlb/odds"
COVERS_ODDS_URL = "https://www.covers.com/sport/baseball/mlb/odds"


REQUIRED_COLUMNS = {
    "game_date",
    "sportsbook",
    "market_type",
    "line_value",
    "snapshot_ts",
}

PLAYER_PROP_MARKET_TYPES = {"pitcher_strikeouts"}

TEAM_ABBR_ALIASES = {
    "WAS": "WSH",
    "CHW": "CWS",
}


def _as_bool(value: object) -> bool:
    if value is None or pd.isna(value):
        return False
    if isinstance(value, bool):
        return value
    return str(value).strip().lower() in {"1", "true", "yes", "y"}


def _ensure_player_prop_markets_table() -> None:
    run_sql(
        """
        CREATE TABLE IF NOT EXISTS player_prop_markets (
            prop_market_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
            game_id BIGINT NOT NULL,
            game_date DATE NOT NULL,
            player_id BIGINT NOT NULL,
            player_name TEXT,
            team VARCHAR(3),
            sportsbook TEXT NOT NULL,
            market_type VARCHAR(32) NOT NULL,
            line_value NUMERIC(5,2),
            over_price INTEGER,
            under_price INTEGER,
            snapshot_ts TIMESTAMPTZ NOT NULL,
            is_opening BOOLEAN DEFAULT FALSE,
            is_closing BOOLEAN DEFAULT FALSE,
            source_name TEXT,
            created_at TIMESTAMPTZ DEFAULT now(),
            updated_at TIMESTAMPTZ DEFAULT now(),
            UNIQUE (game_id, player_id, sportsbook, market_type, snapshot_ts)
        )
        """
    )
    run_sql("CREATE INDEX IF NOT EXISTS idx_player_prop_markets_game ON player_prop_markets(game_id, snapshot_ts DESC)")
    run_sql("CREATE INDEX IF NOT EXISTS idx_player_prop_markets_player ON player_prop_markets(player_id, game_date)")


def _normalize_name(value: str | None) -> str:
    cleaned = re.sub(r"[^a-z0-9]+", " ", (value or "").lower()).strip()
    aliases = {
        "la angels": "los angeles angels",
        "la dodgers": "los angeles dodgers",
        "oakland athletics": "athletics",
        "sacramento athletics": "athletics",
        "d backs": "arizona diamondbacks",
        "diamondbacks": "arizona diamondbacks",
    }
    return aliases.get(cleaned, cleaned)


def _normalize_team_abbr(value: str | None) -> str | None:
    if value is None:
        return None
    cleaned = str(value).strip().upper()
    return TEAM_ABBR_ALIASES.get(cleaned, cleaned)


def _load_games(start_date, end_date) -> pd.DataFrame:
    games = query_df(
        """
        SELECT
            g.game_id,
            g.game_date,
            g.home_team,
            g.away_team,
            ht.team_name AS home_team_name,
            at.team_name AS away_team_name
        FROM games g
        LEFT JOIN dim_teams ht ON ht.team_abbr = g.home_team
        LEFT JOIN dim_teams at ON at.team_abbr = g.away_team
        WHERE g.game_date BETWEEN :start_date AND :end_date
        """,
        {"start_date": start_date, "end_date": end_date},
    )
    if games.empty:
        return games
    games["game_date"] = pd.to_datetime(games["game_date"], errors="coerce").dt.date
    return games


def _parse_covers_date(label: str, default_year: int) -> datetime.date | None:
    raw = " ".join(str(label).strip().replace("\xa0", " ").split())
    if not raw:
        return None
    upper = raw.upper()
    if upper.startswith("TODAY"):
        return datetime.now(timezone.utc).date()
    if upper.startswith("TOMORROW"):
        return datetime.now(timezone.utc).date() + pd.Timedelta(days=1)
    token = raw.split(",", 1)[0].strip()
    try:
        parsed = datetime.strptime(f"{token} {default_year}", "%b %d %Y").date()
    except ValueError:
        return None
    return parsed


def _fetch_covers_rows(start_date, end_date) -> list[dict[str, object]]:
    games = _load_games(start_date, end_date)
    if games.empty:
        return []

    response = requests.get(
        COVERS_ODDS_URL,
        timeout=30,
        headers={"User-Agent": "Mozilla/5.0"},
    )
    response.raise_for_status()
    panel_match = re.search(r'<div role="tabpanel"[^>]*id="tab-total".*?</table>', response.text, re.S)
    if not panel_match:
        log.warning("Covers total tab was not present in the page response")
        return []
    panel_html = panel_match.group(0)
    row_matches = re.findall(r'<tr[^>]*class="oddsGameRow"[^>]*>(.*?)</tr>', panel_html, re.S)

    rows: list[dict[str, object]] = []
    for row_html in row_matches:
        teams = re.findall(r"<strong>([A-Z]{2,3})</strong>", row_html)
        if len(teams) < 2:
            continue
        away_team = _normalize_team_abbr(teams[0])
        home_team = _normalize_team_abbr(teams[1])
        date_match = re.search(r"<div class=\"td-cell game-time\">\s*<span>([^<]+)</span>", row_html)
        if not date_match:
            continue
        game_date = _parse_covers_date(date_match.group(1), end_date.year)
        if game_date is None or game_date < start_date or game_date > end_date:
            continue
        matched = games[
            (games["game_date"] == game_date)
            & (games["home_team"] == home_team)
            & (games["away_team"] == away_team)
        ]
        if matched.empty:
            continue
        game_id = int(matched.iloc[0]["game_id"])

        cell_matches = re.findall(r'<td[^>]*data-book="([^"]+)"[^>]*>(.*?)</td>', row_html, re.S)
        for sportsbook, cell_html in cell_matches:
            outcomes = re.findall(
                r'<div class="td-cell (away|home)-cell">.*?<a[^>]*data-betslip-side="(over|under)"[^>]*>\s*([ou])\s*([0-9]+(?:\.[0-9]+)?)&nbsp;\s*<span class="American __american"[^>]*>([-+0-9]+)</span>',
                cell_html,
                re.S,
            )
            if len(outcomes) < 2:
                continue
            over = next((item for item in outcomes if item[1] == "over"), None)
            under = next((item for item in outcomes if item[1] == "under"), None)
            if over is None or under is None:
                continue
            line_value = float(over[3])
            rows.append(
                {
                    "game_id": game_id,
                    "game_date": game_date,
                    "sportsbook": sportsbook,
                    "market_type": "total",
                    "line_value": line_value,
                    "over_price": int(over[4]),
                    "under_price": int(under[4]),
                    "snapshot_ts": datetime.now(timezone.utc),
                    "is_opening": False,
                    "is_closing": False,
                    "source_name": "covers_html",
                }
            )
    return rows


def _build_team_lookup(games: pd.DataFrame) -> dict[str, str]:
    lookup: dict[str, str] = {}
    for row in games.itertuples(index=False):
        lookup[_normalize_name(row.home_team_name)] = row.home_team
        lookup[_normalize_name(row.away_team_name)] = row.away_team
    return lookup


def _fetch_odds_api_rows(start_date, end_date, api_key: str) -> list[dict[str, object]]:
    games = _load_games(start_date, end_date)
    if games.empty:
        return []
    team_lookup = _build_team_lookup(games)
    response = requests.get(
        ODDS_API_URL,
        params={
            "apiKey": api_key,
            "regions": "us",
            "markets": "totals",
            "oddsFormat": "american",
            "dateFormat": "iso",
        },
        timeout=30,
    )
    response.raise_for_status()
    payload = response.json()

    rows: list[dict[str, object]] = []
    for event in payload:
        commence_ts = pd.to_datetime(event.get("commence_time"), utc=True, errors="coerce")
        if pd.isna(commence_ts):
            continue
        event_date = commence_ts.date()
        if event_date < start_date or event_date > end_date:
            continue
        home_team = team_lookup.get(_normalize_name(event.get("home_team")))
        away_team = team_lookup.get(_normalize_name(event.get("away_team")))
        if not home_team or not away_team:
            continue
        matched = games[
            (games["game_date"] == event_date)
            & (games["home_team"] == home_team)
            & (games["away_team"] == away_team)
        ]
        if matched.empty:
            continue
        game_id = int(matched.iloc[0]["game_id"])
        for bookmaker in event.get("bookmakers", []):
            for market in bookmaker.get("markets", []):
                if market.get("key") != "totals":
                    continue
                outcomes = market.get("outcomes", [])
                over = next((item for item in outcomes if str(item.get("name", "")).lower() == "over"), None)
                under = next((item for item in outcomes if str(item.get("name", "")).lower() == "under"), None)
                point = None
                if over and over.get("point") is not None:
                    point = over.get("point")
                elif under and under.get("point") is not None:
                    point = under.get("point")
                last_update = pd.to_datetime(
                    market.get("last_update") or bookmaker.get("last_update") or datetime.now(timezone.utc),
                    utc=True,
                    errors="coerce",
                )
                rows.append(
                    {
                        "game_id": game_id,
                        "game_date": event_date,
                        "sportsbook": bookmaker.get("key") or bookmaker.get("title") or "odds_api",
                        "market_type": "total",
                        "line_value": point,
                        "over_price": None if not over else over.get("price"),
                        "under_price": None if not under else under.get("price"),
                        "snapshot_ts": last_update.to_pydatetime() if pd.notna(last_update) else datetime.now(timezone.utc),
                        "is_opening": False,
                        "is_closing": False,
                        "source_name": "the_odds_api",
                    }
                )
    return rows


def _load_manual_csv(csv_path: Path) -> pd.DataFrame:
    frame = pd.read_csv(csv_path)
    missing = REQUIRED_COLUMNS - set(frame.columns)
    if missing:
        raise ValueError(f"Missing market columns: {sorted(missing)}")
    frame["game_date"] = pd.to_datetime(frame["game_date"], errors="coerce").dt.date
    frame["snapshot_ts"] = pd.to_datetime(frame["snapshot_ts"], utc=True, errors="coerce")
    frame["line_value"] = pd.to_numeric(frame.get("line_value"), errors="coerce")
    frame["over_price"] = pd.to_numeric(frame.get("over_price"), errors="coerce")
    frame["under_price"] = pd.to_numeric(frame.get("under_price"), errors="coerce")
    if "player_id" in frame.columns:
        frame["player_id"] = pd.to_numeric(frame.get("player_id"), errors="coerce")
    else:
        frame["player_id"] = pd.Series([None] * len(frame), dtype=object)
    if "player_name" not in frame.columns:
        frame["player_name"] = None
    if "team" not in frame.columns:
        frame["team"] = None
    if "home_team" not in frame.columns:
        frame["home_team"] = None
    if "away_team" not in frame.columns:
        frame["away_team"] = None
    if "is_opening" in frame.columns:
        frame["is_opening"] = frame["is_opening"].map(_as_bool)
    else:
        frame["is_opening"] = False
    if "is_closing" in frame.columns:
        frame["is_closing"] = frame["is_closing"].map(_as_bool)
    else:
        frame["is_closing"] = False
    if "source_name" not in frame.columns:
        frame["source_name"] = "manual"
    else:
        frame["source_name"] = frame["source_name"].fillna("manual")
    return frame


def main() -> int:
    parser = argparse.ArgumentParser(description="Import totals and player prop market CSV rows")
    parser.add_argument("--csv", help="Override manual markets csv path")
    add_date_range_args(parser)
    args = parser.parse_args()
    settings = get_settings()
    csv_path = Path(args.csv) if args.csv else settings.manual_markets_csv
    start_date, end_date = resolve_date_range(args)

    inserted = 0
    if settings.odds_api_key:
        try:
            odds_rows = _fetch_odds_api_rows(start_date, end_date, settings.odds_api_key)
        except requests.RequestException as exc:
            log.warning("Odds API market pull failed: %s", exc)
        else:
            inserted += upsert_rows("game_markets", odds_rows, ["game_id", "sportsbook", "market_type", "snapshot_ts"])
            if odds_rows:
                log.info("Imported %s market rows from the Odds API", len(odds_rows))
    else:
        try:
            covers_rows = _fetch_covers_rows(start_date, end_date)
        except requests.RequestException as exc:
            log.warning("Covers market pull failed: %s", exc)
        else:
            inserted += upsert_rows("game_markets", covers_rows, ["game_id", "sportsbook", "market_type", "snapshot_ts"])
            if covers_rows:
                log.info("Imported %s market rows from Covers totals HTML", len(covers_rows))

    if not csv_path.exists():
        log.info("No manual market CSV found at %s", csv_path)
        return 0 if inserted == 0 else 0

    frame = _load_manual_csv(csv_path)

    games = query_df(
        """
        SELECT game_id, game_date, home_team, away_team
        FROM games
        """
    )
    games["game_date"] = pd.to_datetime(games["game_date"]).dt.date
    if "game_id" in frame.columns:
        merged = frame.merge(
            games[["game_id", "game_date", "home_team", "away_team"]],
            on="game_id",
            how="left",
            suffixes=("", "_game"),
        )
        if "game_date_game" in merged.columns:
            merged["game_date"] = merged["game_date"].where(merged["game_date"].notna(), merged["game_date_game"])
        if "home_team_game" in merged.columns:
            merged["home_team"] = merged["home_team"].where(merged["home_team"].notna(), merged["home_team_game"])
        if "away_team_game" in merged.columns:
            merged["away_team"] = merged["away_team"].where(merged["away_team"].notna(), merged["away_team_game"])
    else:
        merged = frame.merge(games, on=["game_date", "home_team", "away_team"], how="left")

    if merged["game_id"].isna().any():
        log.warning("Skipped %s unresolved market rows", int(merged["game_id"].isna().sum()))
        merged = merged[merged["game_id"].notna()].copy()

    if merged.empty:
        return 0

    if "market_type" in merged.columns:
        merged["market_type"] = merged["market_type"].astype(str).str.strip().str.lower()

    player_lookup = query_df("SELECT player_id, full_name, team_abbr FROM dim_players")
    if not player_lookup.empty:
        player_lookup["name_key"] = player_lookup["full_name"].fillna("").astype(str).str.strip().str.lower()
        player_lookup["team_key"] = player_lookup["team_abbr"].fillna("").astype(str).str.strip().str.upper()

    prop_mask = merged["market_type"].isin(PLAYER_PROP_MARKET_TYPES) | merged["player_id"].notna()
    if prop_mask.any() and not player_lookup.empty:
        unresolved_prop_mask = prop_mask & merged["player_id"].isna() & merged["player_name"].notna()
        if unresolved_prop_mask.any():
            resolver = merged.loc[unresolved_prop_mask, ["player_name", "team"]].copy()
            resolver["name_key"] = resolver["player_name"].fillna("").astype(str).str.strip().str.lower()
            resolver["team_key"] = resolver["team"].fillna("").astype(str).str.strip().str.upper()
            resolver = resolver.merge(
                player_lookup[["player_id", "name_key", "team_key", "full_name"]],
                on=["name_key", "team_key"],
                how="left",
            )
            merged.loc[unresolved_prop_mask, "player_id"] = resolver["player_id"].values
            missing_names = merged.loc[unresolved_prop_mask, "player_name"].isna()
            if missing_names.any():
                merged.loc[unresolved_prop_mask & missing_names, "player_name"] = resolver["full_name"].values

    populated = merged[
        merged[["line_value", "over_price", "under_price"]].notna().any(axis=1)
    ].copy()
    skipped_blank = len(merged) - len(populated)
    if skipped_blank:
        log.info("Skipped %s blank manual market template rows", skipped_blank)
    merged = populated

    if merged.empty:
        return 0

    game_rows = []
    prop_rows = []
    for row in merged.itertuples(index=False):
        snapshot_ts = row.snapshot_ts.to_pydatetime() if pd.notna(row.snapshot_ts) else datetime.now(timezone.utc)
        payload = {
            "game_id": int(row.game_id),
            "game_date": row.game_date,
            "sportsbook": row.sportsbook,
            "market_type": row.market_type,
            "line_value": row.line_value,
            "over_price": getattr(row, "over_price", None),
            "under_price": getattr(row, "under_price", None),
            "snapshot_ts": snapshot_ts,
            "is_opening": _as_bool(getattr(row, "is_opening", False)),
            "is_closing": _as_bool(getattr(row, "is_closing", False)),
            "source_name": getattr(row, "source_name", "manual"),
        }
        if row.market_type in PLAYER_PROP_MARKET_TYPES or pd.notna(getattr(row, "player_id", None)):
            if pd.isna(getattr(row, "player_id", None)):
                continue
            prop_rows.append(
                {
                    **payload,
                    "player_id": int(row.player_id),
                    "player_name": getattr(row, "player_name", None),
                    "team": getattr(row, "team", None),
                }
            )
        else:
            game_rows.append(payload)

    if game_rows:
        inserted += upsert_rows("game_markets", game_rows, ["game_id", "sportsbook", "market_type", "snapshot_ts"])
    if prop_rows:
        _ensure_player_prop_markets_table()
        inserted += upsert_rows(
            "player_prop_markets",
            prop_rows,
            ["game_id", "player_id", "sportsbook", "market_type", "snapshot_ts"],
        )
    log.info(
        "Imported %s manual total rows and %s manual player prop rows from %s",
        len(game_rows),
        len(prop_rows),
        csv_path,
    )
    return 0


if __name__ == "__main__":
    raise SystemExit(main())